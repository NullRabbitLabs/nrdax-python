#!/usr/bin/env python3
"""Validate a full NRDAX dataset with nrdax-python and write VALIDATION_REPORT.md.

Loads a dataset (the bundled snapshot by default, or any ``--source`` spec),
exercises loading, schema validation, identifier and relationship integrity,
round-trip fidelity, and STIX export/validation, then writes a Markdown report that
lets the *underlying dataset* be corrected separately. It never normalises or
discards data - it reports.

Usage:
    python scripts/validate_dataset.py [SOURCE_SPEC] [-o VALIDATION_REPORT.md]

SOURCE_SPEC uses the same syntax as the CLI's --source (default: bundled).
"""

from __future__ import annotations

import argparse
import json
from collections import Counter
from pathlib import Path

from nrdax import NRDAX
from nrdax.cli.main import resolve_source
from nrdax.exporters import stix_bundle, technique_ids, validate_bundle
from nrdax.vocab import FAMILIES

_DIRTY_LEN = 60


def _iter_raw(reg: NRDAX):
    """Yield (technique, raw_dict) for round-trip fidelity checks."""
    for t in reg.techniques:
        yield t, t.to_dict()


def build_report(reg: NRDAX, source_spec: str) -> str:
    lines: list[str] = []
    w = lines.append

    reproduced = [t for t in reg if t.is_reproduced]
    known = [t for t in reg if not t.is_reproduced]

    w("# NRDAX dataset validation report")
    w("")
    w(f"Generated by `nrdax-python` against source `{source_spec}`.")
    w("")
    w("## Summary")
    w("")
    w(f"- Dataset version: `{reg.version}`")
    w(f"- DOI: {'`' + reg.doi + '`' if reg.doi else '(none minted)'}")
    w(f"- Schema version targeted: `{reg.schema_version}`")
    w(f"- Techniques loaded: **{len(reg)}** "
      f"({len(reproduced)} reproduced, {len(known)} known-only)")
    issues = reg.validate()
    errs = [i for i in issues if i.severity == "error"]
    warns = [i for i in issues if i.severity == "warning"]
    w(f"- Validation issues: **{len(errs)} error(s), {len(warns)} warning(s)**")
    w(f"- All records loaded without failure: **{'yes' if len(reg) > 0 else 'no'}**")
    w("")

    # -- schema validation issues ---------------------------------------------
    w("## 1. Schema validation")
    w("")
    if not issues:
        w("No schema validation issues. Every record parses cleanly against the "
          "NRDAX contract, with no missing required fields and no unknown vocabulary "
          "values.")
    else:
        w("Issues found (records are still loaded; nothing is discarded):")
        w("")
        for sev, group in (("error", errs), ("warning", warns)):
            for i in group[:50]:
                w(f"- **{sev}** `{i.locator}`: {i.message}")
        if len(issues) > 50:
            w(f"- ... and {len(issues) - 50} more")
    w("")

    # -- round-trip fidelity ---------------------------------------------------
    w("## 2. Round-trip fidelity (representability)")
    w("")
    mismatches = []
    for t, rt in _iter_raw(reg):
        reparsed = NRDAX.from_memory([rt]).get(t.id).to_dict()
        if reparsed != rt:
            mismatches.append(t.id)
    if not mismatches:
        w(f"All **{len(reg)}** records round-trip losslessly "
          "(`from_dict` -> `to_dict` is identity, including preserved unknown "
          "fields). No record is unrepresentable.")
    else:
        w(f"{len(mismatches)} record(s) do not round-trip losslessly: "
          f"{', '.join(mismatches[:20])}")
    w("")

    # -- identifier integrity --------------------------------------------------
    w("## 3. Identifier integrity")
    w("")
    ids = [t.id for t in reg]
    dupes = [k for k, v in Counter(ids).items() if v > 1]
    nums = sorted(int(i.split("T")[-1]) for i in ids if i.startswith("NRDAX-T"))
    gaps = [n for n in range(nums[0], nums[-1] + 1) if n not in set(nums)] if nums else []
    w(f"- Id range: `NRDAX-T{nums[0]:04d}` .. `NRDAX-T{nums[-1]:04d}`")
    w(f"- Duplicate ids: {dupes or 'none'}")
    w(f"- Missing ids in range (gaps): "
      f"{'none' if not gaps else ', '.join(f'NRDAX-T{n:04d}' for n in gaps[:20])}")
    w("")

    # -- relationship / reference targets --------------------------------------
    w("## 4. Relationship and reference target integrity")
    w("")
    known_bad = [kc.technique_id for kc in reg.known_coverage if kc.technique_id not in reg]
    w(f"- Known-coverage entries referencing an unknown technique id: "
      f"{len(known_bad)}{' (' + ', '.join(known_bad[:10]) + ')' if known_bad else ''}")
    w("- NRDAX has no asserted technique-to-technique edges, so there are no internal "
      "relationship targets that can dangle. `related` is derived from shared "
      "family/chain/reference.")
    w("")

    # -- external reference data quality --------------------------------------
    w("## 5. External reference data quality")
    w("")
    kind_counts: Counter[str] = Counter()
    dirty: list[tuple[str, str, str]] = []  # (technique_id, kind, id)
    id_eq_url = 0
    total_refs = 0
    for t in reg:
        for _scope, ref in t.iter_references():
            total_refs += 1
            kind_counts[ref.kind] += 1
            looks_dirty = (
                len(ref.id) > _DIRTY_LEN
                or " " in ref.id
                or ref.id.startswith(("http://", "https://"))
                or "/" in ref.id
            )
            if looks_dirty:
                dirty.append((t.id, ref.kind, ref.id))
            if ref.url is not None and ref.url == ref.id:
                id_eq_url += 1
    w(f"- Total references: {total_refs}")
    w("- By kind: " + ", ".join(f"`{k}`={v}" for k, v in sorted(kind_counts.items())))
    w(f"- References whose `id` is free-text or a URL rather than a clean identifier: "
      f"**{len(dirty)}** "
      f"({100 * len(dirty) / total_refs:.0f}% of references)")
    w(f"- References where `id` duplicates `url`: **{id_eq_url}**")
    if dirty:
        w("")
        w("  These are preserved verbatim (never rewritten). Examples:")
        for tid, kind, rid in dirty[:8]:
            w(f"  - `{tid}` `{kind}`: `{rid[:110]}`")
        by_kind = Counter(k for _, k, _ in dirty)
        w("")
        w("  Dirty-id references are concentrated in: "
          + ", ".join(f"`{k}` ({v})" for k, v in by_kind.most_common()))
        w("")
        w("  **Recommended upstream fix:** for `vendor-advisory` references, store a "
          "short advisory identifier in `id` and move the URL/prose into `url`. This "
          "would make `--reference`/`cve` exact lookups and citations cleaner.")
    w("")

    # -- vocabulary usage ------------------------------------------------------
    w("## 6. Vocabulary usage")
    w("")
    used_fams = Counter(t.family for t in reg)
    unused = [f for f in FAMILIES if f not in used_fams]
    w(f"- Families in use: {len(used_fams)} of {len(FAMILIES)}")
    w(f"- Unused families: {', '.join(unused) if unused else 'none'}")
    w("- Statuses: " + ", ".join(f"`{k}`={v}" for k, v in Counter(t.status for t in reg).items()))
    fids: Counter[str] = Counter()
    origins: Counter[str] = Counter()
    chains: Counter[str] = Counter()
    for t in reg:
        for inst in t.instances:
            fids[inst.fidelity] += 1
            origins[inst.discovery_origin] += 1
            chains[inst.chain] += 1
    w("- Instance fidelity classes: " + ", ".join(f"`{k}`={v}" for k, v in fids.most_common()))
    w("- Instance discovery origins: " + ", ".join(f"`{k}`={v}" for k, v in origins.most_common()))
    w(f"- Distinct chains with a reproduced instance: {len(chains)}")
    w("")

    # -- first_seen ------------------------------------------------------------
    w("## 7. first_seen dates")
    w("")
    fs = Counter(t.first_seen for t in reg)
    top = fs.most_common(1)[0]
    w(f"- Distinct first_seen dates: {len(fs)}")
    w(f"- Most common date: `{top[0]}` on {top[1]} techniques "
      f"({100 * top[1] / len(reg):.0f}% of records)")
    w("- Note: a large cluster on a single recent date usually reflects an import/"
      "catalogue date rather than a true discovery date. Treat `first_seen` as "
      "'first catalogued' where it clusters.")
    w("")

    # -- STIX ------------------------------------------------------------------
    w("## 8. STIX 2.1 export")
    w("")
    bundle = stix_bundle(list(reg), reg.version)
    stix_errors = validate_bundle(bundle)
    rt_ids = technique_ids(bundle)
    w(f"- All {len(reg)} techniques export to a STIX 2.1 bundle.")
    w(f"- Structural validation errors: **{len(stix_errors)}**")
    w(f"- Identity round-trip: **{len(rt_ids)} / {len(reg)}** NRDAX ids recovered "
      "from the bundle.")
    w("- The export is byte-identical to the canonical NRDAX emitter (golden-tested).")
    w("")

    # -- conclusion ------------------------------------------------------------
    w("## Conclusion")
    w("")
    ok = (len(errs) == 0 and not mismatches and not dupes and len(rt_ids) == len(reg)
          and len(stix_errors) == 0)
    if ok:
        w("**The dataset loads completely and is fully representable by this client.** "
          "Every record parses, round-trips losslessly, and exports to valid STIX with "
          "identity preserved. The only data-quality finding is cosmetic: many "
          "`vendor-advisory` references carry free-text/URL `id`s (section 5), which "
          "the client preserves verbatim and which the producer could tidy upstream.")
    else:
        w("Some checks did not pass cleanly; see the sections above. Records are still "
          "loaded and never discarded.")
    w("")
    return "\n".join(lines) + "\n"


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("source", nargs="?", default="bundled", help="source spec (default: bundled)")
    ap.add_argument("-o", "--output", default="VALIDATION_REPORT.md")
    args = ap.parse_args()

    reg = NRDAX.load(resolve_source(args.source))
    report = build_report(reg, args.source)
    Path(args.output).write_text(report, encoding="utf-8")
    print(f"wrote {args.output} ({len(report)} bytes) for {len(reg)} techniques")
    # Also print a JSON summary for CI.
    print(json.dumps({
        "version": reg.version,
        "techniques": len(reg),
        "errors": len([i for i in reg.validate() if i.severity == "error"]),
        "warnings": len([i for i in reg.validate() if i.severity == "warning"]),
    }))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
